void cd(const char *path) {
    if (chdir(path) == -1) {
        perror("cd");
    }
}

// Function to implement ls command
void ls() {
    DIR *dir;
    struct dirent *entry;

    if ((dir = opendir(".")) == NULL) {
        perror("ls");
        return;
    }

    while ((entry = readdir(dir)) != NULL) {
        printf("%s\n", entry->d_name);
    }

    closedir(dir);
}

